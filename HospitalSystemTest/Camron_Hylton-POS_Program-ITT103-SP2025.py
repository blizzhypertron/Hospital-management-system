# ðŸ¥ Main hospital system logic - the mothership
class HospitalSystem:
    def __init__(self):
        self.patients = {}
        self.doctors = {}
        self.appointments_list = {}

    def add_patient(self, name, age, gender):
        # sanity check, this ain't the old folks home ðŸ§“
        if age >= 100 or age <= 0:
            print("Invalid age. That number doesn't exist, you crazy! ðŸ˜­")
            return
        if gender.lower() not in ["male", "female"]:
            print("Please enter 'Male or Female' for gender")
            return

        p = Patient(name, age, gender)
        self.patients[p.patient_id] = p
        print(f"Patient registered with ID: {p.patient_id} âœ…")

    def add_doctor(self, name, specialty, slots):
        # Doc just clocked in â°
        d = Doctor(name, specialty, slots)
        self.doctors[d.doctor_id] = d
        print(f"Doctor added with ID: {d.doctor_id} ðŸ©º")

    def book_appointment(self, pid, did, slot):
        print("Trying to book that appointment... fingers crossed ðŸ¤ž")
        patient = self.patients.get(pid)
        doctor = self.doctors.get(did)
        if not patient or not doctor:
            print("Invalid patient or doctor ID. Bro, check your inputs ðŸ’€")
            return

        if not doctor.is_available(slot):
            print("Doc is booked at that time ðŸ˜¤ Choose another slot.")
            return

        # Secure the bag ðŸ”’
        appointment = Appointment(patient, doctor, slot)
        doctor.book_slot(slot)
        patient.appointments.append(appointment)
        self.appointments_list[appointment.appointment_id] = appointment
        print(f"Appointment confirmed! ID: {appointment.appointment_id} ðŸŽ‰")

    def cancel_appointment(self, aid):
        appt = self.appointments_list.get(aid)
        if appt:
            appt.cancel()
            print("Appointment cancelled. RIP ðŸª¦")
        else:
            print("Appointment not found. That ID's ghosting us ðŸ‘»")

    def generate_bill(self, aid):
        appt = self.appointments_list.get(aid)
        if not appt:
            print("Appointment ID not found. You sure that's real? ðŸ¤¨")
            return

        extras = input("Drop extra charges (comma separated): ")
        extra_costs = list(map(int, extras.split(','))) if extras else []
        bill = Billing(appt, extra_costs)
        bill.print_receipt()

